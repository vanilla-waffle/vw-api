package com.waffle.data.constants.types.vehicle;

/**
 * Car transmission types.
 */
public enum Transmission {

    /**
     * Automated transmission.
     */
    AT,
    /**
     * Manual transmission.
     */
    MT,
    /**
     * Automated manual (hybrid) transmission.
     */
    AMT,
    /**
     * Continuously variable transmission.
     */
    CVT
}
